# Operadores de asignacion

# Comun = (Variable = valor)
numero = 10
print(numero)

#Incremento suma += (A lo que tenga la variable le suma el nuevo valor)
print("-------------[ Incremento (suma) ]------------")
numero_suma = 10
print(numero_suma)
numero_suma += 5
print(numero_suma)
numero_suma = numero_suma + 7
print(numero_suma)

#Decremento resta += (A lo que tenga la variable le resta el nuevo valor)
print("-------------[ Decremento (resta) ]------------")
numero_resta = 20
print(numero_resta)
numero_resta -= 3
print(numero_resta)
numero_resta = numero_resta - 8
print(numero_resta)

#Incremento multiplicacion += (A lo que tenga la variable le multiplica el nuevo valor)
print("-------------[ Incremento (multiplicacion) ]------------")
numero_multiplicacion = 20
print(numero_multiplicacion)
numero_multiplicacion *= 2
print(numero_multiplicacion)
numero_multiplicacion = numero_multiplicacion * 6
print(numero_multiplicacion)

#Decremento division += (A lo que tenga la variable le divide el nuevo valor)
print("-------------[ Decremento (division) ]------------")
numero_division = 100
print(numero_division)
numero_division /= 5
print(numero_division)
numero_division = numero_division / 4
print(numero_division)

